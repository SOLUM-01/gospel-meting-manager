-- 중보기도팀 사진 갤러리에서 이미지 제거
-- Supabase SQL Editor에서 실행하세요!

-- ============================================
-- 1단계: 현재 상태 확인
-- ============================================
SELECT 
  title,
  image_url as "메인 이미지",
  images as "갤러리 이미지"
FROM tasks 
WHERE title = '중보기도팀';

-- ============================================
-- 2단계: 갤러리 이미지만 제거 (메인 이미지는 유지)
-- ============================================
UPDATE tasks
SET 
  images = NULL
WHERE title = '중보기도팀';

-- ============================================
-- 3단계: 결과 확인
-- ============================================
SELECT 
  title,
  image_url as "메인 이미지 (유지됨)",
  images as "갤러리 이미지 (삭제됨)"
FROM tasks 
WHERE title = '중보기도팀';

